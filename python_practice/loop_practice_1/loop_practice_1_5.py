# practice loop 1 문제5



numlist = []
evencount=0
oddcount=0
num = 1
while num != 0:
    numlist.append(int, input("입력하시오"))
    if 0 in numlist == 0:
        break
for x in numlist:
    if x%2==0:
        evencount +=1
    else:
        oddcount+=1
print("odd :",oddcount,"even :",evencount)

